void main()
{
  int a = 12;
  int b = 4;

  // Performing Bitwise AND on a and b
  var c = a & b;
  print(c);

  // Performing Bitwise OR on a and b
  var d = a | b;
  print(d);

  // Performing Bitwise XOR on a and b
  var e = a ^ b;
  print(e);

  // Performing Bitwise NOT on a
  var f = ~a;
  print(f);

  // Performing left shift on a
  var g = a << b;
  print(g);

  // Performing right shift on a
  var h = a >> b;
  print(h);
}